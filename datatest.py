from tqsdk import TqApi, TqAuth
import pandas as pd
import numpy as np

# ==========================================
# 模块 1: 数据清洗函数定义
# 功能：专门针对 TqSdk 获取的上期所 Tick 数据（如 rb2505/ag2506）做专业清洗
# ==========================================
def clean_tq_tick_data(df: pd.DataFrame) -> pd.DataFrame:
    """
    专门针对 TqSdk 获取的 rb2505/ag2506 数据进行清洗
    参数:
        df: 原始 Tick 数据 DataFrame，必须包含 datetime/last_price/volume/amount/bid_price1/ask_price1 等字段
    返回:
        清洗后的高质量 Tick 数据 DataFrame
    """
    # 做一份数据深拷贝，避免修改原始 DataFrame 导致的 SettingWithCopyWarning
    df = df.copy()

    # -------------------------------------------------------------------------
    # 步骤 1: 差分计算真实的 Tick 级成交量与成交额
    # 逻辑说明：
    # TqSdk 返回的 volume/amount 是【累计值】（从开盘到当前的总成交量）
    # 我们需要通过 diff() 做差分，才能得到【当前这根 Tick 单独的成交量/成交额】
    # -------------------------------------------------------------------------
    df['tick_volume'] = df['volume'].diff().fillna(0)  # 差分后第一行是NaN，用0填充
    df['tick_amount'] = df['amount'].diff().fillna(0)

    # 特殊处理：跨交易日或数据断点时，累计值可能重置，导致差分出现负数
    # 逻辑：成交量/成交额不可能为负，遇到负数直接归零
    df.loc[df['tick_volume'] < 0, 'tick_volume'] = 0
    df.loc[df['tick_amount'] < 0, 'tick_amount'] = 0

    # -------------------------------------------------------------------------
    # 步骤 2: 盘口合法性与有效性过滤 (严禁盘口倒挂)
    # 逻辑说明：
    # 正常期货盘口必须满足：卖一价 > 买一价（否则存在无风险套利空间，属于无效数据）
    # 同时过滤掉买一/卖一价为0、或挂单量为0的异常数据（可能是数据传输错误）
    # -------------------------------------------------------------------------
    valid_mask = (
        (df['ask_price1'] > df['bid_price1']) &  # 卖一价必须严格大于买一价（无倒挂）
        (df['bid_price1'] > 0) &                  # 买一价必须大于0（排除无效报价）
        (df['ask_volume1'] > 0) &                 # 卖一挂单量必须大于0（有真实流动性）
        (df['bid_volume1'] > 0)                   # 买一挂单量必须大于0
    )
    df = df[valid_mask].copy()  # 只保留满足所有条件的有效 Tick

    # -------------------------------------------------------------------------
    # 步骤 3: 交易时段精确过滤 (以上期所 螺纹/白银 为例)
    # 逻辑说明：
    # 期货有明确的交易时段，非交易时段的 Tick（如集合竞价前、收盘后）属于无效数据
    # 上期所品种（rb/ag）交易时间：
    #   日盘：09:00-10:15, 10:30-11:30, 13:30-15:00
    #   夜盘：21:00-次日 02:30
    # -------------------------------------------------------------------------
    def is_trading_time(dt):
        """内部辅助函数：判断给定时间戳是否在合法交易时段内"""
        t = dt.time()  # 提取时间部分（时分秒）
        
        # 日盘时段判断
        if (pd.Timestamp('09:00:00').time() <= t <= pd.Timestamp('10:15:00').time()) or \
           (pd.Timestamp('10:30:00').time() <= t <= pd.Timestamp('11:30:00').time()) or \
           (pd.Timestamp('13:30:00').time() <= t <= pd.Timestamp('15:00:00').time()):
            return True
        
        # 夜盘时段判断（注意跨天逻辑：21点后到次日2点半都算）
        if (t >= pd.Timestamp('21:00:00').time()) or (t <= pd.Timestamp('02:30:00').time()):
            return True
        
        # 不在任何交易时段内，返回 False
        return False

    # 对每一行数据应用交易时段判断，只保留交易时段内的 Tick
    df = df[df['datetime'].apply(is_trading_time)].copy()

    # -------------------------------------------------------------------------
    # 步骤 4: 时间戳去重与排序
    # 逻辑说明：
    # 1. 原始数据可能因网络波动、数据重传出现同一时间戳的重复 Tick，需去重（保留最后一条）
    # 2. 确保数据严格按时间递增顺序排列，为后续回测/分析做准备
    # -------------------------------------------------------------------------
    df = df.sort_values('datetime')  # 按时间戳升序排序
    df = df.drop_duplicates(subset=['datetime'], keep='last')  # 去重：同一时间戳只保留最后一条

    # 重置索引（清洗后索引不连续，重置为 0,1,2... 方便后续使用）
    return df.reset_index(drop=True)


